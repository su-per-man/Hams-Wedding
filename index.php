                <h2 id="quoteTitle">How I meet My Match</h2>
                <myQuote id="quoteJinia"><p style="font-family:Tahoma, Geneva, sans-serif">I was going to college…finally. A big day for me, excited, nervous and happy to be finally stepping to the world of dreams. College is always the most excited time for everyone. College is about friends, masti, love and many dreams. One thing I was scared of was college Ragging. I had heard a lot from friends and seniors about the dreadful ragging. 
<br><br>I remember fresh the day we were sitting in our college auditorium waiting for the seniors to get ragged. My nervous eyes were looking at the gang of seniors looking at us like preys. Not literally but it still felt scary. However, things turned out the other way very soon. My eyes saw a guy, tall, dark and handsome. Suddenly the scary feeling turned into excitement wanting the guys to enter early.
<br><br>I don't know what to call it, but for me it was 'Love at First Sight'. The guy was in a Peach Shirt and Formal trousers. Its 7 Years but I still remember the colour of his shirt the first time I saw him. After all, it was love at first sight. 
<br><br>Things started cooking between us very early. It was like having the best college experience. Getting attention from the one you have crush on makes it exciting times even the boring classes. He was my senior and that even made it sweeter. Slowly it was clear he was also interested in me and it were the days of text message with 160 words limit. Still words seemed to be stories coming from him. 
<br><br>For the record, it was 16th August when I first saw him. And later that month on 30th August it came the ultimate day. 
<br><br>To give a slight background, Hambir had a very close group of friends like Tamal Da, Bikram Da, Oindrila Di, Sudip Da, Adi Da and others. Bikram Da on 30th August called me and said me to meet him urgently.
<br><br>I was very confused then, as I thought, he was going to propose me. Sorry Bikram Da but I had thought it then as you had called me so urgently and seriously. Little I knew it was all part of the final plan. He took me to the nearby lanes of my college and said me to wait. I was confused and scared. 
<br><br>Suddenly, I saw Hambir coming on a bike. It was getting a little clear now. I was trying to control the excitement. When Bikram Da left me and Hambir, I was sure what was coming. Hambir asked me "I have something to say to you, Should I be short or long".
<br><br>I said" Let it be Short I have class". He was shaking with nervousness and said to me " I really like you, I love You" and read a poem to me. I was happiest and really excited. But still wanted to have a little fun, so even though I wanted to scream a loud yes, I said "I will Think about It". 
<br><br>Its getting too long now, I am really lucky to get him with me through these years. Finally we are tying our fates together. Wish us Good Wishes and thanks to everyone who helped us get each other. 
</p></myQuote>
				<myQuote id="quoteHams"><p style="font-family:Tahoma, Geneva, sans-serif">Wonder happens when it is most unexpected. Same is my story of meeting my soul mate. It all started with a college ragging (we called it Introduction, decency). It all happened rather by luck, as if we were meant to be together. It just clicked as soon as my eyes meet Jinia’s. I still recall the day we meet on the stairs of our college where we just shared a small chat and from then it all started. Today looking back to it I can say, I am lucky we had meet the way we had.
<br><br>Here I have to mention the timeless efforts and unmatched guidance from my expert friends. Without their help, it wouldn’t have happened. Days were tough then and it needed physical efforts. Unlike today where people propose on WhatsApp. We had only 180 words per text to share our thoughts not unlimited texts like today. And the worst part was finishing up your text limits in between a romantic conversation. It was the pain only 90s kids will know. And remember those days we had Orkut. Now you know this love story has gone through a transition phase but it has still been the same.
<br><br>Aha I just forgot to mention one of the most exciting and daring day in my life. Yeah! I mean the big day, my Proposal to Jinia. Have you felt the nervousness before your 12th boards? Its 10x when you plan to propose to your girl. Like we prepare for our boards, I had prepared and rehearsed my lines whole night. Just like the first day our eyes meet, I thought our college was the best location. Here again my multitalented and expert guides were ready with all the necessary battle weapons. 
<br><br>At last with all the expectation and the nervousness, I had the courage to finally make it happen. I remember it fresh as yesterday, I forgot all the lines I had rehearsed. The only thing I said, “I Love You”. But hold on, life wasn’t easy. Jinia said, “I will think about it”. Trust me it is the longest and toughest time for any guy. 
<br><br>But the end was happy and we finally became each others for lifetime. 
<br><br>Love always wins through every up and downs.
</p></myQuote>
